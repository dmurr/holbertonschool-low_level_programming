#include <stdlib.h>

/**
 * op_add - adds two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: minuend
 * @b: subtruend
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divied two numbers
 * @a: dividend
 * @b: divisor
 *
 * Return: Quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs modulo operation
 * @a: number 1
 * @b: number 2
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
